/**
 * op_add - function
 * Description: op_add
 * @a: a
 * @b: b
 * Return: c
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function
 * Description: op_sub
 * @a: a
 * @b: b
 * Return: c
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function
 * Description: op_mul
 * @a: a
 * @b: b
 * Return: c
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function
 * Description: op_div
 * @a: a
 * @b: b
 * Return: c
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function
 * Description: op_mod
 * @a: a
 * @b: b
 * Return: c
*/
int op_mod(int a, int b)
{
	return (a % b);
}
